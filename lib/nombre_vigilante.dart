class Nombres_api {
  dynamic cedula;
  dynamic nombre;
  dynamic apellido;
  dynamic correo;
  dynamic avatar;
  dynamic fecha;

  
  Nombres_api(cedula, nombre, apellido, correo, avatar, fecha)  {
    this.cedula = cedula;
    this.nombre = nombre;
    this.apellido = apellido;
    this.correo = correo;
    this.avatar = avatar;
    this.fecha = fecha;
  }
}

